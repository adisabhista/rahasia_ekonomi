document.addEventListener('DOMContentLoaded', () => {
    const articleGrid = document.getElementById('article-grid');
    const dictionaryGrid = document.getElementById('dictionary-grid');
    const searchInput = document.getElementById('dictionary-search');
    const menuToggle = document.querySelector('.menu-toggle');
    const navList = document.querySelector('.nav-list');

    // Views
    const heroSection = document.querySelector('.hero');
    const latestSection = document.getElementById('latest');
    const detailSection = document.getElementById('article-detail');
    const dictionarySection = document.getElementById('dictionary');

    // Detail Elements
    const detailTitle = document.getElementById('detail-title');
    const detailCategory = document.getElementById('detail-category');
    const detailDate = document.getElementById('detail-date');
    const detailImage = document.getElementById('detail-image');
    const detailContent = document.getElementById('detail-content');

    // Render Articles List
    function renderArticles() {
        if (articleGrid) {
            articleGrid.innerHTML = articles.map(article => `
            <article class="article-card">
                    <div class="card-image" style="background-image: url('${article.image}')"></div>
                    <div class="card-content">
                        <div class="card-meta">
                            <span class="category">${article.category}</span> â€¢ <span class="date">${article.date}</span>
                        </div>
                        <h3 class="card-title">${article.title}</h3>
                        <p class="card-excerpt">${article.excerpt}</p>
                        <a href="#article/${article.id}" class="card-link">Baca Selengkapnya -></a>
                    </div>
                </article>
    `).join('');
        }
    }

    // Render Dictionary
    function renderDictionary(terms = dictionaryTerms) {
        if (dictionaryGrid) {
            dictionaryGrid.innerHTML = terms.map(item => `
            <div class="dictionary-card">
                    <h3 class="term-title">${item.term}</h3>
                    <p class="term-def">${item.definition}</p>
                </div>
    `).join('');
        }
    }

    // Dictionary Search
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            const keyword = e.target.value.toLowerCase();
            const filtered = dictionaryTerms.filter(item =>
                item.term.toLowerCase().includes(keyword) ||
                item.definition.toLowerCase().includes(keyword)
            );
            renderDictionary(filtered);
        });
    }

    // Router Logic
    function handleRoute() {
        const hash = window.location.hash;

        if (hash.startsWith('#article/')) {
            const id = parseInt(hash.split('/')[1]);
            const article = articles.find(a => a.id === id);

            if (article) {
                showArticle(article);
            }
        } else if (hash === '#dictionary') {
            showDictionary();
        } else {
            showHome();
        }
    }

    function showDictionary() {
        heroSection.style.display = 'none';
        latestSection.style.display = 'none';
        detailSection.style.display = 'none';
        dictionarySection.style.display = 'block';
        renderDictionary();
        window.scrollTo(0, 0);
    }

    function showHome() {
        heroSection.style.display = 'flex';
        latestSection.style.display = 'block';
        detailSection.style.display = 'none';
        dictionarySection.style.display = 'none';
        window.scrollTo(0, 0);
    }

    function showArticle(article) {
        heroSection.style.display = 'none';
        latestSection.style.display = 'none';
        dictionarySection.style.display = 'none';
        detailSection.style.display = 'block';

        // Populate Detail
        detailTitle.textContent = article.title;
        detailCategory.textContent = article.category;
        detailDate.textContent = article.date;
        detailImage.style.backgroundImage = `url('${article.image}')`;

        // Simulate full content (since we only have excerpts in mock data)
        if (article.content) {
            detailContent.innerHTML = article.content;
        } else {
            detailContent.innerHTML = `
                <p class="lead">${article.excerpt}</p>
                <p>Lorem ipsum dolor sit amet, consectetur adipiscing elit. Sed do eiusmod tempor incididunt ut labore et dolore magna aliqua. Ut enim ad minim veniam, quis nostrud exercitation ullamco laboris nisi ut aliquip ex ea commodo consequat.</p>
                <p>Duis aute irure dolor in reprehenderit in voluptate velit esse cillum dolore eu fugiat nulla pariatur. Excepteur sint occaecat cupidatat non proident, sunt in culpa qui officia deserunt mollit anim id est laborum.</p>
                <h3>Analisis Mendalam</h3>
                <p>Sed ut perspiciatis unde omnis iste natus error sit voluptatem accusantium doloremque laudantium, totam rem aperiam, eaque ipsa quae ab illo inventore veritatis et quasi architecto beatae vitae dicta sunt explicabo.</p>
                <p>Nemo enim ipsam voluptatem quia voluptas sit aspernatur aut odit aut fugit, sed quia consequuntur magni dolores eos qui ratione voluptatem sequi nesciunt.</p>
            `;
        }

        window.scrollTo(0, 0);
    }

    // Initial Load
    renderArticles();
    handleRoute();
    window.addEventListener('hashchange', handleRoute);

    // Mobile Menu Toggle
    if (menuToggle) {
        menuToggle.addEventListener('click', () => {
            navList.classList.toggle('active');

            if (navList.classList.contains('active')) {
                navList.style.display = 'flex';
                navList.style.flexDirection = 'column';
                navList.style.position = 'absolute';
                navList.style.top = '80px';
                navList.style.left = '0';
                navList.style.width = '100%';
                navList.style.background = '#0a192f';
                navList.style.padding = '20px';
                navList.style.borderBottom = '1px solid rgba(255,255,255,0.1)';
            } else {
                navList.style.display = '';
            }
        });
    }
});
